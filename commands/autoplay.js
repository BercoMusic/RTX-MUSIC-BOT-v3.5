const { EmbedBuilder, ApplicationCommandOptionType } = require('discord.js');
const db = require("../mongoDB");

module.exports = {
  name: "autoplay",
  description: "Toggle the autoplay mode.",
  permissions: "0x0000000000000800",
  options: [],
  voiceChannel: true,
  run: async (client, interaction) => {
    try {
      const queue = client.player.getQueue(interaction.guild.id);
      if (!queue || !queue.playing) return interaction.reply({ content: '⚠️ No music playing!!', ephemeral: true });

      queue.toggleAutoplay();

      const embed = new EmbedBuilder()
        .setColor(queue.autoplay ? client.config.embedColor : client.config.errorColor)
        .setTitle(client.user.username + " - Autoplay")
        .setDescription(`**Autoplay Mode:** \`${queue.autoplay ? 'Active' : 'Inactive'}\``)
        .setTimestamp()
        .setFooter({ text: 'MusicMaker ❤️' })
      
      return interaction.reply({ embeds: [embed] });
      
    } catch (e) {
      console.error(e);
    }
  },
};

